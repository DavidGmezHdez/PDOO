    public void setCasilla(Casilla cas){
        this.casilla = cas;
        this.Nombre.setText(this.casilla.getNombre());
        
        switch(this.casilla.getTipo()){
            case 0:
                this.Tipo.setVisible(false);
                this.Titulo.setVisible(false);
                this.Valor.setVisible(false);
                this.tipo.setVisible(false);
                this.titulo.setVisible(false);
                this.valor.setVisible(false);
                break;
            case 1:
                this.Tipo.setVisible(true);
                this.Titulo.setVisible(true);
                this.tipo.setVisible(true);
                this.titulo.setVisible(true);
                this.Titulo.setText(this.casilla.toString());
                this.Tipo.setText("Calle");
                
        }
    }

